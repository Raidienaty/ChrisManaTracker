package develop;

public class SaveData
{
    private String username;
    private int max;
    private int current;

    public String getUsername()
    {
        return username;
    }

    public int getMax()
    {
        return max;
    }

    public int getCurrent()
    {
        return current;
    }

    public void setMax(int max)
    {
        this.max = max;
    }

    public void setCurrent(int current)
    {
        this.current = current;
    }
}
